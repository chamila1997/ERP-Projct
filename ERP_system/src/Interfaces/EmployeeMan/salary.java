    public void tableload(){
        
        try {
            String sql = "SELECT empId,name,age,address,address2,address3,nic,phone,department,email,BasicSalary,position,BRA,incentive,transport,epfNo FROM employee";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            empTable.setModel(DbUtils.resultSetToTableModel(rs));
            
          
        } 
        catch (Exception e) {
        }

    }
    
