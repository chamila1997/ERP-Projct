    private void txt_calActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_calActionPerformed
        // TODO add your handling code here:

        int salary = Integer.parseInt(txt_salary.getText());
        int nor_ot = Integer.parseInt(norOTBox.getText());
        int hol_ot = Integer.parseInt(holOTBox.getText());
        
       
        double NormalOvertimeRate = (salary/200) * 1.5;
        double HolidayOvertimeRate = (salary / 200) * 2;

        //calculate the normal  overtime
        double normalOT = nor_ot * NormalOvertimeRate;
        
         //calculate the holiday  overtime
        double holidayOT = hol_ot * HolidayOvertimeRate;
        
        //calculate total OT
        double totalOT = normalOT + holidayOT;
        String t = String.valueOf(totalOT);
        String n = String.valueOf(normalOT);
        String h = String.valueOf(holidayOT);
        normalOTbox.setText(n);
        holidayOTbox.setText(h);
        TotalOvertimeBox.setText(t);
        
        //calculate epf 
        
        double com_epf = salary * 12 / 100;
        double emp_epf = salary * 8 / 100;
        double total_epf = com_epf + emp_epf;
        String z = String.valueOf(total_epf);
        epfBox.setText(z);
        
        //calculate etf
        
        double etf = salary * 3 / 100;
        String e = String.valueOf(etf);
        etfBox.setText(e);
      
        //calculate total salary
        
        //all other additions
        int med = Integer.parseInt(txt_med.getText());
        int otherInc = Integer.parseInt(otherIncnBox.getText());
        int attend = Integer.parseInt(attendanceBox.getText());
        int add = med+otherInc+attend;
        
        //all other deductions
        int otherded = Integer.parseInt(otherdedBox.getText());
        int loan = Integer.parseInt(loanBox.getText());
        int nopay = Integer.parseInt(nopayBox.getText());
        int ded = otherded+loan+nopay;
        
        
        double calc = (totalOT + add + salary) - (emp_epf + ded);
        String c = String.valueOf(calc);
        lbl_total.setText(c);
    }//GEN-LAST:event_txt_calActionPerformed
    
