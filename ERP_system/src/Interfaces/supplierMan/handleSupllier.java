        String company      = supplierTable.getValueAt(r, 2).toString();
        String address = supplierTable.getValueAt(r, 3).toString();
        String phone = supplierTable.getValueAt(r, 4).toString();
        String email = supplierTable.getValueAt(r, 5).toString();

        //assigning textbox values
        namebox.setText(name);                              
        companybox.setText(company);
        addressbox.setText(address);
        phonebox.setText(phone);
        emailbox.setText(email);
  
        
        IDlabel.setText(id);
