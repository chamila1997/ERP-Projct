    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        //delete empployee
        int x = JOptionPane.showConfirmDialog(null, "Do you really want to delete"); //confirmation message

        if(x==0){

            String id = IDlabel.getText();

            String q = "DELETE FROM supplier WHERE sid ='"+id+"'";

            try {
                pst = con.prepareStatement(q);
                pst.execute();

                tableload(); //refresh table

                namebox.setText(null);
                companybox.setText(null);
                addressbox.setText(null);
                phonebox.setText(null);
                emailbox.setText(null);

            } catch (Exception e) {
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

