    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String Project_name = project_namebox.getText();
        String Estimated_Cost = Estimated_costbox.getText();
        String Location = locationbox.getText();
        String Field = fieldbox.getText();
        String Time_period = Estimated_time_Periodbox.getText();
        String Starting_date = starting_datebox.getText();
        String Ending_date = ending_datebox.getText();
        String Description = descriptionbox.getText();
        String q = "INSERT INTO project (Project_name,Estimated_Cost,Location,Field,Time_period,Starting_date,Ending_date,Description) Values('"+Project_name+"','"+Estimated_Cost+"','"+Location+"','"+Field+"','"+Time_period+"','"+Starting_date+"','"+Ending_date+"','"+Description+"')";
        try{
            pst = con.prepareStatement(q);
            pst.execute();
            
            project_namebox.setText(null);   //clear when project add
        Estimated_costbox.setText(null);
        locationbox.setText(null);
        Estimated_time_Periodbox.setText(null);
        starting_datebox.setText(null);
        ending_datebox.setText(null);
        fieldbox.setText(null);
        descriptionbox.setText(null);

        }catch (Exception e){   
        
           JOptionPane.showConfirmDialog(null,e);
        }

        
        
        if(Project_name.equals("")){
            JOptionPane.showMessageDialog(null,"Project Name is Mandotory");
        }
        if(Location.equals("")){
            JOptionPane.showMessageDialog(null,"Location is Mandotory");
        }
        if(Starting_date.equals("")){
            JOptionPane.showMessageDialog(null,"Starting Date is Mandotory");
        }
        if(Ending_date.equals("")){
            JOptionPane.showMessageDialog(null,"Ending Date is Mandotory");
        
        {
            JOptionPane.showMessageDialog(null,"Ending Date Less than Starting Date");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed
    }
